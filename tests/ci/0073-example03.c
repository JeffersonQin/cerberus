#include <stdio.h>


int y1[4][3] = {
      { 1, 3, 5 },
      { 2, 4, 6 },
      { 3, 5, 7 },
};

int y2[4][3] = {
       1, 3, 5, 2, 4, 6, 3, 5, 7
};

int main(void)
{
  printf("y1[0][0] = %d, y1[0][1] = %d, y1[0][2] = %d\n",
         y1[0][0], y1[0][1], y1[0][2]);
  printf("y1[1][0] = %d, y1[1][1] = %d, y1[1][2] = %d\n",
         y1[1][0], y1[1][1], y1[1][2]);
  printf("y1[2][0] = %d, y1[2][1] = %d, y1[2][2] = %d\n",
         y1[2][0], y1[2][1], y1[2][2]);
  
  printf("y2[0][0] = %d, y2[0][1] = %d, y2[0][2] = %d\n",
         y2[0][0], y2[0][1], y2[0][2]);
  printf("y2[1][0] = %d, y2[1][1] = %d, y2[1][2] = %d\n",
         y2[1][0], y2[1][1], y2[1][2]);
  printf("y2[2][0] = %d, y2[2][1] = %d, y2[2][2] = %d\n",
         y2[2][0], y2[2][1], y2[2][2]);
}
